class Movies {
  late String nameMovie;
  late String tahunRilis;
  late String rating;
  late String genre;
  late String posterPath;

  Movies(String nameMovie, String tahunRilis, String rating, String genre, String posterPath) {
    this.nameMovie = nameMovie;
    this.tahunRilis = tahunRilis;
    this.rating = rating;
    this.genre = genre;
    this.posterPath = posterPath;
  }
}
